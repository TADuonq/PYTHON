st = "Tự học lập trình Python"
hoa = st.upper()
thuong = st.lower()
print("Chuoi in hoa: ", hoa)
print("Chuoi in thuong: ", thuong)
print("Chuoi co ki tu dau tien viet hoa: ", hoa.capitalize())
print("Chuoi co ki tu dau tu viet hoa: ", hoa.title())
print("Doi hoa thuong: ", hoa.title().swapcase())
